import hashlib
var1 = hashlib.md5(b'abc123')
var2 = hashlib.sha1(b'abc123')
var3 = hashlib.sha224(b'abc123')
var4 = hashlib.sha256(b'abc123')
var5 = hashlib.sha384(b'abc123')
var6 = hashlib.sha512(b'abc123')
print("md5:  ",var1.hexdigest())
print("sha1:  ",var2.hexdigest())
print("sha224:  ",var3.hexdigest())
print("sha256:  ",var4.hexdigest())
print("sha384:  ",var5.hexdigest())
print("sha512:  ",var6.hexdigest())
